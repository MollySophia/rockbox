#include "config.h"
#include "system.h"
#include "lcd.h"
#include "backlight.h"
#include "backlight-target.h"

void backlight_hw_on(void)
{
    LCD1_CONTROL |= 0x02;
    lcd_set_backlight_inversion(true);
}

void backlight_hw_off(void)
{
    LCD1_CONTROL &= ~0x02;
    lcd_set_backlight_inversion(false);
}
