static void perm_it(unsigned char *data, int length)
{
    int i, j;
    for(i = 0; (i + 12) < length; i += 12)
    {
        unsigned char perm[8];
        perm[0] = data[i + 1];
        perm[1] = data[i + 7];
        perm[2] = data[i + 2];
        perm[3] = data[i + 4];
        perm[4] = data[i + 0];
        perm[5] = data[i + 6];
        perm[6] = data[i + 3];
        perm[7] = data[i + 5];
        for(j = 0; j < 8; j++)
            data[i + j] = perm[j];
    }
}

static void xor_it(unsigned char *data, int length)
{
    int i, j;
    for(i = 0; i < length; i += 4)
    {
        unsigned char Ekey = xor_key[xor_key[data[i]] + 256];
        for(j = 1; j < 4; j++)
            data[i + j] ^= Ekey;
    }
}

int encrypt(unsigned char *data, int length)
{
     if (length % 512)
         return -1;

     xor_it(data, length);
     perm_it(data, length);

     return 0;
}
