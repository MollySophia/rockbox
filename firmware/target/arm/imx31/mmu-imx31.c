#include "cpu.h"
#include "mmu-imx31.h"
#include "mmu-arm.h"

unsigned long addr_virt_to_phys(unsigned long addr)
{
    return addr | CSD0_BASE_ADDR;
}

unsigned long addr_phys_to_virt(unsigned long addr)
{
    return addr & ~CSD0_BASE_ADDR;
}
