#include "config.h"
#include "jz4740.h"
#include "audiohw.h"

#define SPEAKER_PIN (32*2 + 27)

void audiohw_enable_speaker(bool on)
{
    __gpio_as_output(SPEAKER_PIN);
    if(on)
        __gpio_set_pin(SPEAKER_PIN);
    else
        __gpio_clear_pin(SPEAKER_PIN);
}
